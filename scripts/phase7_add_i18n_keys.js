/**
 * Set a dot-notation key in a nested object.
 */
function setNestedKey(obj, dotKey, value) {
  const parts = dotKey.split('.');
  let current = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    if (!current[parts[i]] || typeof current[parts[i]] !== 'object') {
      current[parts[i]] = {};
    }
    current = current[parts[i]];
  }
  current[parts[parts.length - 1]] = value;
}

let updated = 0;
for (const locale of LOCALES) {
  const filePath = path.join(LOCALES_DIR, `${locale}.json`);
  if (!fs.existsSync(filePath)) {
    console.warn(`SKIP: ${filePath} does not exist`);
    continue;
  }

  const raw = fs.readFileSync(filePath, 'utf8');
  const json = JSON.parse(raw);

  const localeKeys = KEYS[locale] || KEYS['en']; // Fallback to English
  for (const [dotKey, value] of Object.entries(localeKeys)) {
    setNestedKey(json, dotKey, value);
  }

  fs.writeFileSync(filePath, JSON.stringify(json, null, 2) + '\n', 'utf8');
  updated++;
  console.log(`OK: ${locale}.json`);
}

console.log(`\nDone: ${updated}/${LOCALES.length} locales updated.`);
